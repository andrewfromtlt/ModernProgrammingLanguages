#include "lab07_shapes.h"
#include <iostream>

int main() {
    std::srand(std::time(NULL));

    int gd = DETECT, gm;
    // Инициализация графического режима. 
    initgraph(&gd, &gm, (char*)"");

    // Проверка на ошибку инициализации графики
    if (graphresult() != grOk) {
        std::cout << "[ERROR] Graphics error: Could not initialize BGI. Make sure WinBGIm is configured.\n";
        return 1;
    }

    setbkcolor(BLACK);
    cleardevice();

    // Создаем объекты [cite: 474, 475]
    Krug testKrug(150, 40, 50, YELLOW);
    Ring testRing(450, 80, 50, GREEN, 10);

    // Запускаем перемещение первого объекта
    // (Для остановки полета и перехода к следующему объекту нужно нажать любую клавишу)
    testKrug.Fly(100);
    
    // Запускаем перемещение второго объекта (С демонстрацией анимации по Заданию 2)
    testRing.Fly(60);

    // Повторяем для круга (как в оригинале)
    testKrug.Fly(60);

    // Зачищаем экран перед выходом
    testKrug.Hide();
    testRing.Hide();
    
    closegraph();
    return 0;
}